			</div>
			<div class="col-md-8 accordian-body collapse in" id="Entrega_Pruebas">
				<div class="row">
				<ul>
					<li><p>El sobre de las pruebas debe venir con nombre y seccion del curso, nombre del profesor y nombre del ayudante que te entrega.</p></li>
					<li><p>El sobre de la prueba debe venir en buenas condiciones (no roto, si esto sucede parcharlo)</p></li>
					<li><p>Junto con las pruebas debe venir la lista de asistencia del curso (los alumnos que rinden su prueba deben firmar la entrega). Con esto nosotros podemos corroborar que la cantidad de pruebas entregadas sea la correcta de acuerdo a los alumnos que firmaron la lista. (con esto evitamos problemas de que se pueda perder alguna prueba una vez que sean entregadas.</p></li>
					<li><p>Cuando un ramo tiene mas de una seccion y las pruebas se trabajan en conjunto, deben corroborar que las secciones no sean mezcladas, ya que esto genera un retraso en la digitalizacion de las pruebas (eMarking no ordena de forma automática las secciones, solo asocia las pruebas a los alumnos de la seccion respectiva.</p></li>
					<li><p>Si las pruebas son corregidas de forma manual, y se usa corrector por algún motivo, verificar que este ya se encuentre seco, asi evitamos que las páginas se pegan y genere problema al momento de escanear las pruebas.</p></li>
				</ul>
